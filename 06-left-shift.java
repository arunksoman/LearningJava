class OperatorExample{  
	public static void main(String args[]){  
	System.out.println(10<<2);
	// shifting left by one bit equal to multiplication by power of 2^1. ie., here it is multiplication by 2^2
	System.out.println(10<<3);
	// shifting left by one bit equal to multiplication by power of 2^1. ie., here it is multiplication by 2^3
	System.out.println(20<<2);
	
	System.out.println(15<<4);//15*2^4=15*16=240  
	}
}  